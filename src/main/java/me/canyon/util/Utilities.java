    public void deleteFile(File file) {
        if (file.exists()) {
            File files[] = file.listFiles();

            for (int i = 0; i < files.length; i++) {
                if (files[i].isDirectory())
                    deleteFile(files[i]);
                else
                    files[i].delete();
            }

            file.delete();
        }
    }

    private final NavigableMap<Long, String> suffixes = new TreeMap<>();
    {
        suffixes.put(1_000L, "k");
        suffixes.put(1_000_000L, "M");
        suffixes.put(1_000_000_000L, "B");
        suffixes.put(1_000_000_000_000L, "T");
        suffixes.put(1_000_000_000_000_000L, "P");
        suffixes.put(1_000_000_000_000_000_000L, "E");
    }

    public String formatEconomy(long value) {
        //Long.MIN_VALUE == -Long.MIN_VALUE so we need an adjustment here
        if (value == Long.MIN_VALUE) return formatEconomy(Long.MIN_VALUE + 1);
        if (value < 0) return "-" + formatEconomy(-value);
        if (value < 1000) return Long.toString(value); //deal with easy case

        Map.Entry<Long, String> e = suffixes.floorEntry(value);
        Long divideBy = e.getKey();
        String suffix = e.getValue();

        long truncated = value / (divideBy / 10); //the number part of the output times 10
        boolean hasDecimal = truncated < 100 && (truncated / 10d) != (truncated / 10);
        return hasDecimal ? (truncated / 10d) + suffix : (truncated / 10) + suffix;
    }

    final static char symbol[] = {'M','D','C','L','X','V','I'};
    final static int value[] = {1000,500,100,50,10,5,1};

    public static int romanToInteger(String roman)
    {
        roman = roman.toUpperCase();
        if(roman.length() == 0) return 0;
        for(int i = 0; i < symbol.length; i++)
        {
            int pos = roman.indexOf(symbol[i]) ;
            if(pos >= 0)
                return value[i] - romanToInteger(roman.substring(0,pos)) + romanToInteger(roman.substring(pos+1));
        }
        throw new IllegalArgumentException("Invalid Roman Symbol.");
    }

    private static int[] numbers = { 1000,  900,  500,  400,  100,   90,
            50,   40,   10,    9,    5,    4,    1 };

    private static String[] letters = { "M",  "CM",  "D",  "CD", "C",  "XC",
            "L",  "XL",  "X",  "IX", "V",  "IV", "I" };

    public static String integerToRoman(int N)
    {
        String roman = "";
        for (int i = 0; i < numbers.length; i++) {
            while (N >= numbers[i]) {
                roman += letters[i];
                N -= numbers[i];
            }
        }
        return roman;
    }

    private final static int CENTER_TAB_PX = 82;

    public static String getCenteredTabListText(String message){
        int messagePxSize = 0;
        boolean previousCode = false;
        boolean isBold = false;

        for(char c : message.toCharArray()){
            if(c == 'ยง'){
                previousCode = true;
                continue;
            }else if(previousCode == true){
                previousCode = false;
                if(c == 'l' || c == 'L'){
                    isBold = true;
                    continue;
                }else isBold = false;
            }else{
                DefaultFontInfo dFI = DefaultFontInfo.getDefaultFontInfo(c);
                messagePxSize += isBold ? dFI.getBoldLength() : dFI.getLength();
                messagePxSize++;
            }
        }

        int halvedMessageSize = messagePxSize / 2;
        int toCompensate = CENTER_TAB_PX - halvedMessageSize;
        int spaceLength = DefaultFontInfo.SPACE.getLength() + 1;
        int compensated = 0;
        StringBuilder sb = new StringBuilder();
        while(compensated < toCompensate){
            sb.append(" ");
            compensated += spaceLength;
        }
        return sb.toString() + message;
    }

